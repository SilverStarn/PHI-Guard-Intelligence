function layoutNodes(nodes: GraphNodeDto[], findingById: Map<string, FindingSummary>): Node[] {
  const lanes: Record<string, { x: number; y: number; count: number }> = {
    database: { x: 0, y: 90, count: 0 },
    schema: { x: 210, y: 90, count: 0 },
    table: { x: 420, y: 0, count: 0 },
    view: { x: 420, y: 0, count: 0 },
    export: { x: 420, y: 0, count: 0 },
    column: { x: 670, y: 0, count: 0 },
    role: { x: 120, y: 360, count: 0 },
    external_destination: { x: 940, y: 110, count: 0 },
    ai_tool: { x: 940, y: 250, count: 0 },
    finding: { x: 1190, y: 0, count: 0 },
    control: { x: 1480, y: 70, count: 0 }
  };
  return nodes.map((item) => {
    const lane = lanes[item.type] ?? lanes.table;
    const yStep = item.type === "finding" ? 98 : item.type === "column" ? 88 : item.type === "table" ? 118 : 92;
    const position = { x: lane.x, y: lane.y + lane.count * yStep };
    lane.count += 1;
    const finding = findingById.get(item.id);
    const nodeHelp = graphNodeHelp(item, finding);
    const tags = item.classifications.slice(0, 3);
    return {
      id: item.id,
      position,
      data: {
        label: (
          <div className="flow-card">
            <div className="flow-kicker">
              <span className="flow-type">{item.type.replace("_", " ")}</span>
              <InfoTooltip text={nodeHelp} />
            </div>
            <strong>{finding ? finding.title : item.label}</strong>
            {tags.length > 0 && (
              <em>{tags.join(", ")}{item.classifications.length > tags.length ? ` +${item.classifications.length - tags.length}` : ""}</em>
            )}
            {item.rowCount ? <small>{item.rowCount.toLocaleString()} rows</small> : null}
          </div>
        )
      },
      className: `risk-node ${riskClass(finding?.risk_score ?? item.riskScore)} ${item.type}`,
      style: {
        width: item.type === "finding" ? 240 : item.type === "column" ? 230 : 190
      }
    };
  });
}

function graphNodeHelp(item: GraphNodeDto, finding?: FindingSummary) {
  if (finding) {
    return `${finding.severity.toUpperCase()} finding scored ${finding.risk_score}: ${finding.description}`;
  }
  if (item.type === "column") {
    const labels = item.classifications.length ? item.classifications.join(", ") : "no PHI-risk labels";
    return `${item.label} is a column with ${labels}. Risk score ${item.riskScore}.`;
  }
  if (item.type === "table" || item.type === "view" || item.type === "export") {
    return `${item.label} is a ${item.type} with rolled-up risk score ${item.riskScore}. Row estimate: ${item.rowCount?.toLocaleString() ?? "unknown"}.`;
  }
  if (item.type === "role") {
    return `${item.label} is a modeled principal. Edges show which PHI assets it can read or administer.`;
  }
  if (item.type === "control") {
    return `${item.label} is a HIPAA-oriented control mapping used to explain why findings matter.`;
  }
  return item.description || `${item.label} is a ${item.type.replace("_", " ")} node in the Intelligence graph.`;
}

function layoutEdges(edges: GraphEdgeDto[]): Edge[] {
  return edges.map((edge) => ({
    id: edge.id,
    source: edge.source,
    target: edge.target,
    label: edge.type === "CONTAINS" ? undefined : edge.label,
    animated: edge.type === "EXPORTED_TO" || edge.type === "SENT_TO" || edge.type === "DERIVES_FROM",
    markerEnd: { type: MarkerType.ArrowClosed, width: 18, height: 18 },
    className: `edge-${edge.type.toLowerCase().replaceAll("_", "-")}`,
    style: { strokeWidth: edge.type === "READ_BY" ? 1.2 : 1.8 }
  }));
}

function riskClass(score: number) {
  if (score >= 85) return "critical";
  if (score >= 70) return "high";
  if (score >= 40) return "moderate";
  return "low";
}

function statusClass(status: string) {
  if (status === "Not ready") return "critical";
  if (status === "Review") return "moderate";
  return "low";
}

function labelize(value: string) {
  return value.replaceAll("_", " ").replace(/\b\w/g, (char) => char.toUpperCase());
}

function formatDate(value: string) {
  return new Intl.DateTimeFormat("en-US", {
    month: "short",
    day: "numeric",
    year: "numeric",
    hour: "numeric",
    minute: "2-digit"
  }).format(new Date(value));
}
