const uploadSuccessHoldMs = 1450;

async function fetchAppData(): Promise<AppData> {
  const [source, summary, graph, findings, deidentification, access, remediations, report, scanRuns, auditEvents] = await Promise.all([
    api.source(),
    api.summary(),
    api.graph(),
    api.findings(),
    api.deidentification(),
    api.accessMatrix(),
    api.remediations(),
    api.report(),
    api.scanRuns(),
    api.auditEvents()
  ]);
  return { source, summary, graph, findings, deidentification, access, remediations, report, scanRuns, auditEvents };
}

export function App() {
  const [activeTab, setActiveTab] = useState<Tab>("dashboard");
  const [data, setData] = useState<AppData | null>(null);
  const [selectedFindingId, setSelectedFindingId] = useState<string | null>(null);
  const [selectedFinding, setSelectedFinding] = useState<FindingDetail | null>(null);
  const [error, setError] = useState<string | null>(null);
  const [actionError, setActionError] = useState<string | null>(null);
  const [actionBusy, setActionBusy] = useState(false);
  const [uploadOverlay, setUploadOverlay] = useState<UploadOverlayState | null>(null);
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    let cancelled = false;
    async function load() {
      try {
        setLoading(true);
        const nextData = await fetchAppData();
        if (cancelled) return;
        setData(nextData);
        setSelectedFindingId(nextData.findings[0]?.id ?? null);
        setError(null);
      } catch (err) {
        if (cancelled) return;
        setError(err instanceof Error ? err.message : "Unable to load intelligence data");
      } finally {
        if (!cancelled) setLoading(false);
      }
    }
    load();
    return () => {
      cancelled = true;
    };
  }, []);

  async function refreshAfterSourceChange() {
    const nextData = await fetchAppData();
    setData(nextData);
    setSelectedFindingId(nextData.findings[0]?.id ?? null);
    setActiveTab("dashboard");
  }

  async function handleUpload(files: File[], projectName: string) {
    const safeProjectName = projectName.trim() || "Uploaded Data Project";
    try {
      setActionBusy(true);
      setActionError(null);
      setUploadOverlay({ phase: "analyzing", projectName: safeProjectName, fileCount: files.length });
      await api.upload(files, safeProjectName);
      await refreshAfterSourceChange();
      setUploadOverlay({ phase: "complete", projectName: safeProjectName, fileCount: files.length });
      window.setTimeout(() => {
        setUploadOverlay((current) => (current?.phase === "complete" ? null : current));
      }, uploadSuccessHoldMs);
    } catch (err) {
      setActionError(err instanceof Error ? err.message : "Upload scan failed");
      setUploadOverlay(null);
    } finally {
      setActionBusy(false);
    }
  }

  async function handleResetDemo() {
    try {
      setActionBusy(true);
      setActionError(null);
      await api.resetDemo();
      await refreshAfterSourceChange();
    } catch (err) {
      setActionError(err instanceof Error ? err.message : "Could not reset demo scan");
    } finally {
      setActionBusy(false);
    }
  }

  useEffect(() => {
    let cancelled = false;
    if (!selectedFindingId) {
      setSelectedFinding(null);
      return;
    }
    api
      .finding(selectedFindingId)
      .then((finding) => {
        if (!cancelled) setSelectedFinding(finding);
      })
      .catch(() => {
        if (!cancelled) setSelectedFinding(null);
      });
    return () => {
      cancelled = true;
    };
  }, [selectedFindingId]);

  if (loading) {
    return (
      <Shell activeTab={activeTab} onTab={setActiveTab}>
        <div className="loading">
          <RefreshCcw className="spin" size={22} />
          <span>Loading scan</span>
        </div>
      </Shell>
    );
  }

  if (error || !data) {
    return (
      <Shell activeTab={activeTab} onTab={setActiveTab}>
        <section className="empty-state">
          <AlertTriangle size={24} />
          <h2>API unavailable</h2>
          <p>{error}</p>
          <code>$env:PYTHONPATH="apps/scanner"; python -m uvicorn apps.api.app.main:app --reload --port 8000</code>
        </section>
      </Shell>
    );
  }

  return (
    <Shell
      activeTab={activeTab}
      onTab={setActiveTab}
      blocked={uploadOverlay !== null}
      overlay={uploadOverlay ? <UploadAnalysisOverlay state={uploadOverlay} /> : null}
    >
      {activeTab === "import" && (
        <ImportPanel
          source={data.source}
          scanRuns={data.scanRuns}
          auditEvents={data.auditEvents}
          busy={actionBusy}
          error={actionError}
          onUpload={handleUpload}
          onResetDemo={handleResetDemo}
        />
      )}
      {activeTab === "dashboard" && (
        <Dashboard
          summary={data.summary}
          findings={data.findings}
          onOpenFinding={(id) => {
            setSelectedFindingId(id);
            setActiveTab("findings");
          }}
        />
      )}
      {activeTab === "map" && (
        <DataMap
          graph={data.graph}
          findings={data.findings}
          onOpenFinding={(id) => {
            setSelectedFindingId(id);
            setActiveTab("findings");
          }}
        />
      )}
      {activeTab === "findings" && (
        <FindingsPanel
          findings={data.findings}
          selectedFinding={selectedFinding}
          selectedFindingId={selectedFindingId}
          onSelect={setSelectedFindingId}
        />
      )}
      {activeTab === "remediation" && (
        <RemediationPanel
          payload={data.remediations}
          onOpenFinding={(id) => {
            setSelectedFindingId(id);
            setActiveTab("findings");
          }}
        />
      )}
      {activeTab === "deid" && <DeidentificationPanel rows={data.deidentification} />}
      {activeTab === "access" && <AccessPanel matrix={data.access} />}
      {activeTab === "report" && <ReportPanel report={data.report} />}
    </Shell>
  );
}

